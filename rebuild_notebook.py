        "def backward_verify(problem_text: str, answer: int, domain: str) -> bool:\n",
        "    \"\"\"\n",
        "    Backward verification: generate code that substitutes the answer back\n",
        "    into the problem constraints and checks all conditions hold.\n",
        "    \"\"\"\n",
        "    if not VLLM_AVAILABLE or llm is None:\n",
        "        return True\n",
